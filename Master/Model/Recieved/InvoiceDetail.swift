// MARK: - OtherPrice
struct OtherPrice: Codable {
    let title, price: String
    let hash: String?
}

enum Price: Codable {
    case integer(Int)
    case string(String)

    init(from decoder: Decoder) throws {
        let container = try decoder.singleValueContainer()
        if let x = try? container.decode(Int.self) {
            self = .integer(x)
            return
        }
        if let x = try? container.decode(String.self) {
            self = .string(x)
            return
        }
        throw DecodingError.typeMismatch(Price.self, DecodingError.Context(codingPath: decoder.codingPath, debugDescription: "Wrong type for Price"))
    }

    func encode(to encoder: Encoder) throws {
        var container = encoder.singleValueContainer()
        switch self {
        case .integer(let x):
            try container.encode(x)
        case .string(let x):
            try container.encode(x)
        }
    }
}

// MARK: - SelectedFeatures
struct SelectedFeatures: Codable {
    let amountOfCream, rang, type: String

    enum CodingKeys: String, CodingKey {
        case amountOfCream = "amount of cream"
        case rang
        case type = "Type"
    }
}

